<script>
    $("#show-answer").prop("disabled", true);

    const answers = [
         $q1 = "<?php echo $a11 ?>",
         $q2 = "<?php echo $a10 ?>",
         $q3 = "<?php echo $a12 ?>",
         $q4 = "<?php echo $a3 ?>",
