    $("#show-answer").prop("disabled", true);

    const answers = [
         $q1 = "<?php echo $a6 ?>",
         $q2 = "<?php echo $a2 ?>",
         $q3 = "<?php echo $a6 ?>",
         $q4 = "<?php echo $a15 ?>",
         $q5 = "<?php echo $a6 ?>",
